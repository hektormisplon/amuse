httpServer.listen(config.nodePort, config.address, () => {
    logger.log({ level: 'info', message: `Serving http://${config.address}:${config.nodePort}/` });
});

// seeder setup
if (config.nodeEnvironment === 'Development') {
    const seeder = new Seeder();
    seeder.seed();
}

export default app;
